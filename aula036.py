'''
Operadores de atribuição: =, +=, -=, *=, /=, //=, **=, %=
'''

# atribuindo
contador = 1
print(contador)

# somando e atribuindo
contador+=120
print(contador)

# multiplicando e atribuindo
contador*=4
print(contador)

# dividindo e atribuindo
contador/=2
print(contador)

# divisão inteira e atribuindo 
contador//=3
print(contador)

# exponenciando e atribuindo 
contador**=10
print(contador)

# resto da divisao e atribuindo
contador%=2
print(contador)

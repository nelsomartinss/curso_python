# Concatenação e repetição

# O operador de adição também serve para concatenar se estiver usando ele com duas strings
concatencao = 'Nelson' + ' ' + 'Martins' # o resultado será Nelson Martins 

# O operador de multiplicação irá fazer a repetição se ele for usado em conjunto com uma string

repetir_tres_vezes = 'nelson' * 3 # ele ira repetir a string 3 vezes

print(concatencao)
print(repetir_tres_vezes)
# if e else - identação e fluxo do interpretador

condicao = True 

if condicao: # se 'condição' - assumindo que é True, existir
    print(True) # os blocos dentro do if e else precisam ter 4 espaços de identação
else: # precisam ter sempre os :
    print(False)

print('isso tá fora do else') # o código não identado não está dentro da condição


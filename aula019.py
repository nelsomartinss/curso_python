# Operadores de comparação
'''
> maior
>= maior ou igual
< menor
<= menor ou igual 
== igual
!= diferente
'''
# sempre que usarmos esses opradores o resultado será booleano

maior = 2 > 1 # 2 é maior do que 1 ?  True
maior_ou_igual = 1 >= 1 # 1 é maior ou igual a 1 ? True
menor = 1 < 2 # 1 é menor do que 2 ? True
menor_ou_igual = 2 <= 2 # 2 é menor ou igual a 2 ? True
igual = 1 == 1 # 1 é igual a 1 ? True
diferente = 'nelson' != 'maria' #'nelson' é igual a 'maria' ? True

print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)
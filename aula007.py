# Variáveis precisam de um nome e esse nome não poder começar com letras maiusculas (por convensão), numeros, underlines e simbolos

# Inicie o nome com letras minusculas, separe as palavras com underline (sneake case)

# Sempre utilize nomes claros para as variaveis, o nome precisa refletir exatamente o que esta na variável

# exemplos:
primeiro_nome = 'Nelson' # string
segundo_nome = 'Martins' # string
idade = 24 # int
altura = 1.74 # float 
peso = 65.7 # float
